import os
import sys

from crewai import Agent, Task, Crew, LLM

# Import the existing system components
from agent import build_and_run_crew, get_reviews

import os
from dotenv import load_dotenv
load_dotenv()

# This Evaluator uses Llama 3.1 via Ollama!
try:
    evaluator_llm = LLM(
        model="ollama/llama3.1:latest",
        base_url="http://localhost:11434",
        api_key="NA",
        temperature=0.1
    )
except Exception as e:
    print(f"Error connecting to local Qwen model: {e}")
    sys.exit(1)

judge_agent = Agent(
    role="QA Systems Grader",
    goal="Evaluate how perfectly an AI summarization model reflects its original source texts.",
    backstory="You are an uncompromising, strict Quality Assurance AI. You do not write summaries. Instead, you critically grade reports written by a different AI model.",
    verbose=True,
    allow_delegation=False,
    llm=evaluator_llm
)

def evaluate_summary(query: str):
    print(f"============================================================")
    print(f" STARTING BENCHMARK FOR QUERY: '{query}'")
    print(f"============================================================")
    
    # 1. Run original system to test (currently Qwen 2.5 14B)
    print("\n>>> Phase 1: Qwen 2.5 14B generating the JSON extraction...")
    try:
        summary_result = build_and_run_crew(query=query)
    except Exception as e:
        print(f"Original pipeline failed: {e}")
        return

    # 2. Grab original raw context
    print("\n>>> Fetching original raw context injected into the prompt...")
    context = get_reviews(query=query)
    
    # 3. Evaluate Results using Judge (Llama 3.1)
    print("\n>>> Phase 2: Llama 3.1 executing QA evaluation on the outputs...")
    evaluation_task = Task(
        description=f"""
        Please review the following source string (Original Customer Reviews from database):
        [SOURCE]:
        {context}
        
        Now, critique the parsed JSON response generated by the AI:
        [GENERATED Output]:
        {summary_result}
        
        Provide a deep evaluation:
        1. Rate Accuracy (1-10): Did the generated output confidently reflect feelings from the raw source?
        2. Rate Hallucinations (1-10): Did the AI make up strengths or weaknesses not present in the [SOURCE] text?
        3. Rate Formatting Pass/Fail: Did it properly separate exactly 5 strengths and 5 weaknesses?
        
        Output directly as a Grading Report block. Do not converse.
        """,
        expected_output="Detailed grading criteria out of 10.",
        agent=judge_agent
    )
    
    eval_crew = Crew(
        agents=[judge_agent],
        tasks=[evaluation_task],
        verbose=True
    )
    
    grade = eval_crew.kickoff()
    
    print("\n\n============================================================")
    print(" 🔎 FINAL LLAMA 3.1 EVALUATION REPORT")
    print("============================================================")
    print(grade.raw)
    print("============================================================\n")

if __name__ == "__main__":
    test_queries = ["dell laptop"]
    for query in test_queries:
        evaluate_summary(query)
